class Product{

    constructor(id, imagen, nombre, talla, piezas, adicional, color, precio, categoria){
        this.id = id;
        this.imagen = imagen;
        this.nombre = nombre;
        this.talla = talla;
        this.piezas = piezas;
        this.adicional = adicional;
        this.color = color;
        this.precio = precio;
        this.categoria = categoria;
    }

}

export default Product;